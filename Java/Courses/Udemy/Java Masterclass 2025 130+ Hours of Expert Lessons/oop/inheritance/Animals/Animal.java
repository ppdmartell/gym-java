class Animal {

    private String type;
    private String size;
    private double weight;

    public Animal() {}

    public Animal(String type, String size, double weight) {
        this.type = type;
        this.size = size;
        this.weight = weight;
    }

    public String getSize() {
        return this.size;                          //This is the proper way to return a field method for a parent class instead of making it protected
    }

}
